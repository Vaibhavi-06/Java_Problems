class VariableTest{
	public static void main(String arg[])
{
		byte a=1;
		short b=5;
		int no=10;
		long d=1001;
		Boolean status=true;
		float f=1.5f;
		double df=13.5;
		char c='s';

		System.out.println("Value of byte: "+a);
		System.out.println("Value of short: "+b);
		System.out.println("Value of int: "+no);
		System.out.println("Value of long: "+d);
		System.out.println("Value of boolean: "+status);
		System.out.println("Value of float: "+f);
		System.out.println("Value of double: "+df);
		System.out.println("Value of char: "+c);
}
}