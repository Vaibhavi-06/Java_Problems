class trafficsignal{
	public static void main(String args[])
	{
		String color ="Red";

		if(color=="Red")
		{
		System.out.println("Stop...");
		}
		else if(color=="Yellow")
		{
		System.out.println("Ready to go...");
		}
		else if(color=="Green")
		{
		System.out.println("Safely go...");
		}
		else
		{
		System.out.println("Invalid color");
		}


	
	}
}